export const humanizeContent = async (text: string, tone: HumanizeTone, pov: PointOfView, customPersona: string, contentType: 'ad' | 'blog' | 'storyboard' = 'blog'): Promise<string> => {
    let prompt: string;

    switch (contentType) {
        case 'ad':
            prompt = HUMANIZE_AD_COPY_PROMPT_TEMPLATE;
            break;
        case 'blog':
        case 'storyboard':
        default:
            prompt = HUMANIZE_CONTENT_PROMPT_TEMPLATE;
            break;
    }
    
    const toneOption = HUMANIZE_TONE_OPTIONS.find(t => t.id === tone);
    // FIX: Property 'vi' does not exist on type '{ en: ... }'. Corrected translations object type inference by fixing vi object.
    const toneName = toneOption ? translations.vi[toneOption.nameKey] : 'người dùng đời thường';
    const povDescription = POV_MAP[pov] || "ngôi thứ hai (bạn)";
    const customPersonaInstruction = customPersona.trim() 
        ? `Additionally, adopt this specific personality: "${customPersona.trim()}". This is more important than the general tone.`
        : '';

    prompt = prompt.replace('{tone}', toneName);
    prompt = prompt.replace('{point_of_view}', povDescription);
    prompt = prompt.replace('{custom_persona_instruction}', customPersonaInstruction);
    prompt = prompt.replace('<<<ORIGINAL_TEXT>>>', text);
    
    const response = await ai.models.generateContent({
        model: textModel,
        contents: prompt,
    });

    return response.text;
};

