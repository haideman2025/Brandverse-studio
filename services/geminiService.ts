export const generateEcomImage = async (prompt: string, images: (UploadedFile | null)[], retries = 3, delay = 2000): Promise<string> => {
    const imageParts = images
        .filter((image): image is UploadedFile => image !== null && !!image.base64)
        .map(image => ({
            inlineData: { data: image.base64!, mimeType: image.type }
        }));

    const parts: any[] = [...imageParts, { text: prompt }];

    for (let i = 0; i <= retries; i++) {
        try {
            const response = await ai.models.generateContent({
                model: imageModel,
                contents: { parts },
            });

            for (const part of response.candidates?.[0]?.content?.parts ?? []) {
                if (part.inlineData) {
                    return `data:${part.inlineData.mimeType};base64,${part.inlineData.data}`;
                }
            }
            throw new Error("No image was generated by the API.");
        } catch (error: any) {
            const errorMessage = error?.message || String(error);
            const isQuotaError = errorMessage.toLowerCase().includes("quota") || 
                                errorMessage.toLowerCase().includes("rate limit") ||
                                errorMessage.toLowerCase().includes("429");
            
            if (isQuotaError && i < retries) {
                const waitTime = delay * Math.pow(2, i);
                console.warn(`Quota exceeded. Retrying in ${waitTime}ms... (Attempt ${i + 1}/${retries})`);
                await new Promise(resolve => setTimeout(resolve, waitTime));
                continue;
            }
            throw error;
        }
    }
    throw new Error("Failed to generate image after multiple retries.");
};

export const extractImagePart = async (base64ImageData: string, mimeType: string, partToExtract: 'face' | 'product' | 'outfit'): Promise<string> => {
    let promptText = '';
    switch (partToExtract) {
        case 'face':
            promptText = "From the provided image, create and return a new image that is a tight, clean, high-quality crop focusing only on the person's face. Preserve the original quality and lighting.";
            break;
        case 'product':
            promptText = "From the provided image, create and return a new image that is a clean, high-quality crop of ONLY the main product. The product should be centered and isolated from the background as much as possible. If you cannot confidently identify a single main product, return the original image without any changes.";
            break;
        case 'outfit':
            promptText = "From the provided image, create and return a new image that is a high-quality crop showing the person's outfit from the torso down, or the most representative part of their clothing. Do not include the face.";
            break;
    }

    const parts = [
        { inlineData: { data: base64ImageData, mimeType: mimeType } },
        { text: promptText }
    ];

    const response = await ai.models.generateContent({
        model: imageModel,
        contents: { parts },
        config: { responseModalities: [Modality.IMAGE] },
    });

    for (const part of response.candidates?.[0]?.content?.parts ?? []) {
        if (part.inlineData) {
            return `data:${part.inlineData.mimeType};base64,${part.inlineData.data}`;
        }
    }
    throw new Error(`Failed to extract ${partToExtract} from the image.`);
};

